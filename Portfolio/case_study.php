    <h2>Findings:</h2>
    <ol class="number_list">
    <li>On average, there was not a significant difference regarding cycling between different calendar years and bike sales and fatalities did not have a strong correlation. Some of the recorded in this case were Olympic years, while others were not, and this caused local maxima on most of these years for annual bike sales. </li>
    <li>From significance testing, we concluded that males had significantly higher fatality rates. In addition, from the visuals, the fatality rates are significantly worse for men between the ages of 50-64. </li>
    <li>No hypothesis test concluded that there were statistically significant differences in fatalites when comparing weekend and weekdays, time of day, or seasons within those time frames. </li>
