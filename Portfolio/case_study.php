    <h2>Overview:</h2>
    <p>The transportation industry has evolved due to the increase of technology and innovation, so there are many options for transportation to get around town. 
        Cycling remains a popular choice of transportation as growing urban cities begin to crowd that is appealing commuting and exercise. Since cyclist are still on the streets, 
        it is important that we are continuing to find ways to reduce the risk that people run when they hop on their bikes to go to work every morning or take their families for a Sunday ride. 
        To reduce the risk that cyclists face, we must investigate the common factors that contribute to increasing pedicyclist fatalities. Some factors that increase fatalities include: increasing bike sales, season, time of day, day of the week, and population size. 
        This report analyzes and discusses the different factors that could contribute to pedicyclist crashes. Our hypothesis determines if there is a correlation between the the common factors listed above and cycling fatalities. <em>Factors Affecting Pedicyclist Crashes</em> applies data science methods using Python to clean, combine, and analyze several datasets to determine the culprits of cycling fatalities.</p>
