    <h2>Overview:</h2>
    <p> Covid-19 has impacted the way that people connect with each other. Many universities across the country continued the semesters with virtual instructions and some gave the in person option. 
        Regardless of the situations, many students were affected by the difficulty to connect with other students, especially those who have never experienced a semester on campus. 
        Friendz is a mobile app that helps college freshmen or anyone new to a college campus, meet new people and make connections despite Covid-19 restrictions. Users can chat with new connections, match with others based on common interest, and a cool feature allows users to get notified when they are in close proximity to someone who has the app and has at least one thing in common with them.
        This app is meant to make it easier for users to find a study buddy in their major or someone who loves dancing as much as they do! Over the course of 3 months, I worked with 3 classmates to conduct user interviews, create a storyboard, paper sketches UI Frames, develop a persona, perform usabilty testing, gather insights, and co-design two iterations of a product that achieves our purpose. This process was documented across 5 reports and a video showing the before and after of our design.
    </p>
    
    <h2>Goals:</h2>
    <ol class="number_list">
    <li> Allow college students to meet others in safe and socially distant ways</li>
    <li> Help students develop strong social groups at Cornell  </li>
    </ol>

    <h2>Design Problems:</h2>
    <ul class = bullets>
    <li> App should be both unique and interactive</li>
    <li> App should facilitate both virtual and safe in-person interactions</li>
    <li> App should ensure users are strong matches so they can develop strong social groups</li>
    </ul>

    <h2>User Findings:</h2>
    <ul class = number_list>
    <li> Zoom and online interaction makes it hard to make real genuine friendships</li>
    <li> Coronavirus restrictions significantly limit interactions, depending on how much students chose to abide by them</li>
    <li> Despite COVID restrictions, students are still finding ways to socialize, especially in dorms and organizations</li>
    <li> Students wish they were able to interact more in-person</li>
    </ul>

    <h2>Usability Tasks:</h2>
    <ul class = no_bullets>
    <li> Task 1: Input personal information and get matches</li>
    <li> Task 2: Chat with individuals over text and voice/video call</li>
    <li> Task 3: Meeting a potential match after getting physically close to them.</li>
    <li> Task 4: Schedule meetings between matches and add them to a calendar</li>
</ul>
    <h2>Design Insights:</h2>
    <p><em>Prior to making the final design changes we gathered some insights from our usability tests: </em> </p>
    <ul class = bullets>
    <li> Some minor changes in design could make certain tasks easier or richer, experiencewise. </li>
    <li> A brighter color scheme may better fit the social nature of the app. </li>
    <li> Make the task flow more intuitive  (i.e. implement swiping where it asks to swipe) </li>
    <li> Provide status updates to users so they can better tell what the app is doing </li>
    </ul>

